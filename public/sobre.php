        <h1>Sobre o site</h1>
        <p>Na atualidade, é vivido mundialmente uma nova pandemia, mais precisamente a do corona vírus COVID-19. Não somente a COVID-19, mas também as pandemias passadas deixaram marcas na história da humanidade com o número de óbitos e pessoas doentes em grave estado de saúde.</p>
        
        <p>Esta página, denominado Consulta COVID-19, tem o objetivo de entregar aos usuários informações com dados oficiais sobre a situação do corona vírus COVID-19. Os usuários podem consultar de maneira simples e objetiva dados atualizados para guiarem os seus cuidados de distanciamento social e assim evitar a propagação da doença.</p>
        
        <p>Utilizamos o banco de dados oficial do Governo Federal do Brasil para entregar melhor as informações atualizadas e verídicas, oferecido pelo Ministério da Saúde (https://covid.saude.gov.br).</p> 
        
        <p>A página pode ser acessada por qualquer usuário que possuir acesso à internet em aparelhos smart ou desktop. São seguidos os padrões e métricas do W3C (World Wide Web Consortium) e o e-MAG (Web content accessibility guidelines) para tornar o conteúdo da web acessível a uma ampla gama de pessoas, incluindo aquelas com algum tipo de limitação.</p> 
        
        <p>A página também é navegável utilizando somente o teclado. Dessa maneira, pessoas com algum tipo de dificuldade/deficiência motora que impossibilite de usar o mouse, também poderão utilizar o sistema.</p> 

